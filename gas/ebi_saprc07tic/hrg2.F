      SUBROUTINE HRG2( DTC )
C**********************************************************************
C
C  FUNCTION: To solve for the concentration of HO, HO2, HONO, and
C            HNO4 alebraically.
C
C  PRECONDITIONS: For the SAPRC07TIC family of mechanisms
C
C  KEY SUBROUTINES/FUNCTIONS CALLED: None
C
C  REVISION HISTORY: Created by EBI solver program, Jan. 9, 2013
C
C**********************************************************************
      USE HRDATA

      IMPLICIT NONE


C..INCLUDES: None


C..ARGUMENTS:
      REAL DTC                      ! Time step

C..PARAMETERS: None


C..EXTERNAL FUNCTIONS: NONE


C..SAVED LOCAL VARIABLES:
      CHARACTER( 16 ), SAVE :: PNAME = 'HRG2'             ! Program name


C..SCRATCH LOCAL VARIABLES:
      REAL    O1D_S                 ! sum of O1D loss frequencies
      REAL    OH_S                  ! stoich coeff for OH from O1D+H2O or H2
      REAL    HO2_S                 ! stoich coeff for HO2 from O1D+H2 rxn
      REAL    EXN_S                 ! sum of NO2EX loss frequencies
      REAL    XOH_S                 ! stoich coeff for OH & HONO from NO2EX loss rxn
      REAL    R4_19                 ! production of OH from HONO
      REAL    R19_4                 ! production of HONO from OH
      REAL    R4_5                  ! production of OH from HO2
      REAL    R5_4                  ! production of HO2 from OH
      REAL    R5_21                 ! production of HO2 from HNO4
      REAL    R21_5                 ! production of HNO4 from HO2
      REAL    P4, P5, P19, P21      ! species production form other paths
      REAL    L4, L5, L19, L21      ! species loss terms

      REAL    A, B, C               ! coeffs of quadratic eq. for HO2
      REAL    Q                     ! intermediate term

      REAL    T1, T2, T3            ! intermediate terms

      REAL    L21_INV               ! reciprocal of HNO4 loss term

C**********************************************************************


c..stoichiometric coefficient for production of HOX from O3 via O1D
      O1D_S =           RKI(  20 )                            ! O1D=2*OH
     &       +          RKI(  21 )                            ! O1D=O3P

      OH_S =   2.000 * RKI(  20 )                            ! O1D=2*OH

      OH_S  = OH_S / O1D_S


c..Production of HO from HO2 (r4,5 terms )
      R4_5 =           RKI(  31 ) * YC( NO               )   ! HO2+NO=OH+NO2
     &      +          RKI(  36 ) * YC( O3               )   ! HO2+O3=OH
     &      +  0.800 * RKI(  39 ) * YC( NO3              )   ! HO2+NO3=0.8*OH+...
     &      +          RKI( 561 ) * YC( CL               )   ! HO2+CL=OH+CLO
     &      +  0.120 * RKI( 674 ) * YC( ISOPO2           )   ! HO2+ISOPO2=...
     &      +  0.175 * RKI( 777 ) * YC( IEPOXOO          )   ! HO2+IEPOXOO=...

      R4_5  = R4_5 * DTC

c..Production of HO from HONO (r4,19 terms )
      R4_19 =           RKI(  23 )                            ! HONO=OH+NO

      R4_19 = R4_19 * DTC

c..Remaining HO production
      P4 =   OH_S  * RXRAT(  18 )      ! O1D=2*OH
     &    +          RXRAT(  28 )      ! HNO3=OH+NO2
     &    +  0.390 * RXRAT(  34 )      ! HNO4=0.39*OH+0.61*HO2+...
     &    +  2.000 * RXRAT(  41 )      ! HO2H=2*OH
     &    +          RXRAT(  49 )      ! NO2EX=OH+HONO
     &    +          RXRAT( 118 )      ! xOH+NO=OH+NO
     &    +          RXRAT( 120 )      ! xOH+NO3=OH+NO3
     &    +  0.500 * RXRAT( 121 )      ! xOH+MEO2=0.5*OH+MEO2
     &    +  0.500 * RXRAT( 122 )      ! xOH+RO2C=0.5*OH+RO2C
     &    +  0.500 * RXRAT( 123 )      ! xOH+RO2XC=0.5*OH+RO2XC
     &    +          RXRAT( 124 )      ! xOH+MECO3=OH+MECO3
     &    +          RXRAT( 125 )      ! xOH+RCO3=OH+RCO3
     &    +          RXRAT( 126 )      ! xOH+BZCO3=OH+BZCO3
     &    +          RXRAT( 127 )      ! xOH+MACO3=OH+MACO3
     &    +          RXRAT( 217 )      ! COOH=OH+HO2+HCHO
     &    +          RXRAT( 219 )      ! ROOH=OH+HO2+RCHO
     &    +          RXRAT( 221 )      ! R6OOH=OH+0.142*HO2+...
     &    +          RXRAT( 223 )      ! RAOOH=OH+HO2+0.5*GLY+...
     &    +  0.826 * RXRAT( 241 )      ! AFG1+O3=0.826*OH+0.522*HO2+...
     &    +  0.826 * RXRAT( 244 )      ! AFG2+O3=0.826*OH+0.522*HO2+...
     &    +  0.471 * RXRAT( 247 )      ! AFG3+O3=0.471*OH+0.554*HO2+...
     &    +  0.208 * RXRAT( 248 )      ! MACR+O3=0.208*OH+0.108*HO2+...
     &    +  0.164 * RXRAT( 250 )      ! MVK+O3=0.164*OH+0.064*HO2+...
     &    +  0.285 * RXRAT( 254 )      ! IPRD+O3=0.285*OH+0.4*HO2+...
     &    +  0.330 * RXRAT( 264 )      ! ACROLEIN+O3=0.33*OH+...
     &    +  0.178 * RXRAT( 267 )      ! ACROLEIN=0.178*OH+...
     &    +          RXRAT( 269 )      ! CCOOOH=OH+CO2+MEO2
     &    +          RXRAT( 271 )      ! RCOOOH=OH+xCCHO+yROOH+CO2+xHO2
     &    +  0.160 * RXRAT( 504 )      ! ETHENE+O3=0.16*OH+0.16*HO2+...
     &    +  0.350 * RXRAT( 508 )      ! PROPENE+O3=0.35*OH+...
     &    +  0.080 * RXRAT( 512 )      ! BUTADIENE13+O3=0.08*OH+...
     &    +  0.266 * RXRAT( 515 )      ! ISOPRENE+O3=0.266*OH+...
     &    +  0.728 * RXRAT( 518 )      ! APIN+O3=0.728*OH+0.009*HO2+...
     &    +  0.500 * RXRAT( 522 )      ! ACETYLENE+O3=0.5*OH+...
     &    +  0.128 * RXRAT( 536 )      ! OLE1+O3=0.128*OH+0.095*HO2+...
     &    +  0.443 * RXRAT( 540 )      ! OLE2+O3=0.443*OH+0.094*HO2+...
     &    +  0.499 * RXRAT( 546 )      ! TERP+O3=0.499*OH+0.078*HO2+...
     &    +  0.499 * RXRAT( 550 )      ! SESQ+O3=0.499*OH+0.078*HO2+...
     &    +          RXRAT( 571 )      ! HOCL=OH+CL
     &    +  0.414 * RXRAT( 587 )      ! ROOH+CL=0.414*OH+HCL+...
     &    +  0.145 * RXRAT( 588 )      ! R6OOH+CL=0.145*OH+HCL+...
     &    +  0.139 * RXRAT( 589 )      ! RAOOH+CL=0.139*OH+...
     &    +          RXRAT( 680 )      ! HPALD=OH+HO2+0.5*HACET+...
     &    +  0.790 * RXRAT( 696 )      ! HC5+O3=0.79*OH+0.6*HO2+...
     &    +  0.730 * RXRAT( 703 )      ! ISOPND+O3=0.73*OH+0.39*HO2+...
     &    +  0.340 * RXRAT( 710 )      ! ISOPNB+O3=0.34*OH+0.08*HO2+...
     &    +  0.850 * RXRAT( 725 )      ! NIT1+O3=0.15*OH+0.45*HO2+...
     &    +  0.950 * RXRAT( 777 )      ! HO2+IEPOXOO=1.125*OH+...
     &    +  0.125 * RXRAT( 778 )      ! IEPOXOO+NO=0.125*OH+...
     &    +  0.063 * RXRAT( 779 )      ! IEPOXOO+MEO2=0.063*OH+...
     &    +  0.063 * RXRAT( 780 )      ! IEPOXOO+RO2C=0.063*OH+...
     &    +  0.125 * RXRAT( 781 )      ! IEPOXOO+MECO3=0.125*OH+...
     &    +          RXRAT( 782 )      ! ISOPOOH=OH+0.91*HO2+...
     &    +  0.330 * RXRAT( 788 )      ! MACR=0.33*OH+0.67*HO2+...
     &    +          RXRAT( 811 )      ! xOH+IMACO3=OH+IMACO3

      P4 = YC0( OH ) + P4 * DTC

c..Production of HO2 from OH ( r5,4 terms )
      R5_4 =           RKI(  26 ) * YC( NO3              )   ! OH+NO3=HO2+NO2
     &      +          RKI(  29 ) * YC( CO               )   ! OH+CO=HO2+CO2
     &      +          RKI(  30 ) * YC( O3               )   ! OH+O3=HO2
     &      +          RKI(  42 ) * YC( HO2H             )   ! OH+HO2H=HO2
     &      +          RKI(  44 ) * YC( SO2              )   ! OH+SO2=HO2+SULF+...
     &      +          RKI(  45 )                            ! OH=HO2
     &      +          RKI( 200 ) * YC( HCHO             )   ! OH+HCHO=HO2+CO
     &      +          RKI( 212 ) * YC( MEOH             )   ! OH+MEOH=HO2+HCHO
     &      +          RKI( 213 ) * YC( HCOOH            )   ! OH+HCOOH=HO2+CO2
     &      +  0.148 * RKI( 222 ) * YC( RAOOH            )   ! OH+RAOOH=...
     &      +  0.630 * RKI( 226 ) * YC( GLY              )   ! OH+GLY=0.63*HO2+...
     &      +  0.472 * RKI( 257 ) * YC( PRD2             )   ! OH+PRD2=...
     &      +  0.189 * RKI( 259 ) * YC( RNO3             )   ! OH+RNO3=...
     &      +  0.300 * RKI( 521 ) * YC( ACETYLENE        )   ! OH+ACETYLENE=...
     &      +  0.570 * RKI( 523 ) * YC( BENZENE          )   ! OH+BENZENE=...
     &      +  0.181 * RKI( 524 ) * YC( TOLUENE          )   ! OH+TOLUENE=...
     &      +  0.159 * RKI( 525 ) * YC( MXYL             )   ! OH+MXYL=...
     &      +  0.161 * RKI( 526 ) * YC( OXYL             )   ! OH+OXYL=...
     &      +  0.159 * RKI( 527 ) * YC( PXYL             )   ! OH+PXYL=...
     &      +  0.022 * RKI( 528 ) * YC( TRIMETH_BENZ124  )   ! OH+...
     &      +  0.950 * RKI( 529 ) * YC( ETOH             )   ! OH+ETOH=0.95*HO2+...
     &      +  0.123 * RKI( 543 ) * YC( ARO1             )   ! OH+ARO1=...
     &      +  0.077 * RKI( 544 ) * YC( ARO2             )   ! OH+ARO2=...
     &      +  0.750 * RKI( 759 ) * YC( HOCCHO           )   ! OH+HOCCHO=...
     &      +  0.825 * RKI( 760 ) * YC( HACET            )   ! OH+HACET=...
     &      +  0.500 * RKI( 772 ) * YC( DHMOB            )   ! OH+DHMOB=0.5*HO2+...
     &      +          RKI( 783 ) * YC( RNO3I            )   ! OH+RNO3I=HO2+NO2+...
     &      +  0.190 * RKI( 839 ) * YC( IMAPAN           )   ! OH+IMAPAN=...

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =           RKI(  33 )                            ! HNO4=HO2+NO2
     &       +  0.610 * RKI(  34 )                            ! HNO4=0.61*HO2+...

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =           RXRAT(  50 )      ! MEO2+NO=HO2+HCHO+NO2
     &    +          RXRAT(  53 )      ! MEO2+NO3=HO2+HCHO+NO2
     &    +  2.000 * RXRAT(  55 )      ! MEO2+MEO2=2*HO2+2*HCHO
     &    +  0.500 * RXRAT(  59 )      ! RO2C+MEO2=0.5*HO2+...
     &    +  0.500 * RXRAT(  64 )      ! RO2XC+MEO2=0.5*HO2+...
     &    +  0.900 * RXRAT(  73 )      ! MECO3+MEO2=0.9*HO2+...
     &    +          RXRAT(  83 )      ! RCO3+MEO2=HO2+HCHO+RO2C+...
     &    +          RXRAT(  94 )      ! BZCO3+MEO2=HO2+HCHO+RO2C+...
     &    +          RXRAT( 108 )      ! xHO2+NO=HO2+NO
     &    +          RXRAT( 110 )      ! xHO2+NO3=HO2+NO3
     &    +  0.500 * RXRAT( 111 )      ! xHO2+MEO2=0.5*HO2+MEO2
     &    +  0.500 * RXRAT( 112 )      ! xHO2+RO2C=0.5*HO2+RO2C
     &    +  0.500 * RXRAT( 113 )      ! xHO2+RO2XC=0.5*HO2+RO2XC
     &    +          RXRAT( 114 )      ! xHO2+MECO3=HO2+MECO3
     &    +          RXRAT( 115 )      ! xHO2+RCO3=HO2+RCO3
     &    +          RXRAT( 116 )      ! xHO2+BZCO3=HO2+BZCO3
     &    +          RXRAT( 117 )      ! xHO2+MACO3=HO2+MACO3
     &    +  2.000 * RXRAT( 198 )      ! HCHO=2*HO2+CO
     &    +          RXRAT( 201 )      ! HCHO+NO3=HO2+HNO3+CO
     &    +          RXRAT( 203 )      ! CCHO=HO2+CO+MEO2
     &    +          RXRAT( 206 )      ! RCHO=HO2+xHO2+yROOH+xCCHO+...
     &    +          RXRAT( 217 )      ! COOH=HO2+OH+HCHO
     &    +          RXRAT( 219 )      ! ROOH=HO2+OH+RCHO
     &    +  0.142 * RXRAT( 221 )      ! R6OOH=0.142*HO2+OH+...
     &    +          RXRAT( 223 )      ! RAOOH=HO2+OH+0.5*GLY+...
     &    +  2.000 * RXRAT( 224 )      ! GLY=2*HO2+2*CO
     &    +  0.630 * RXRAT( 227 )      ! GLY+NO3=0.63*HO2+HNO3+...
     &    +          RXRAT( 228 )      ! MGLY=HO2+CO+MECO3
     &    +  0.522 * RXRAT( 241 )      ! AFG1+O3=0.522*HO2+0.826*OH+...
     &    +  1.023 * RXRAT( 242 )      ! AFG1=1.023*HO2+0.173*MEO2+...
     &    +  0.522 * RXRAT( 244 )      ! AFG2+O3=0.522*HO2+0.826*OH+...
     &    +  0.554 * RXRAT( 247 )      ! AFG3+O3=0.554*HO2+0.471*OH+...
     &    +  0.108 * RXRAT( 248 )      ! MACR+O3=0.108*HO2+0.208*OH+...
     &    +  0.064 * RXRAT( 250 )      ! MVK+O3=0.064*HO2+0.164*OH+...
     &    +  0.400 * RXRAT( 254 )      ! IPRD+O3=0.4*HO2+0.285*OH+...
     &    +  1.233 * RXRAT( 256 )      ! IPRD=1.233*HO2+0.467*MECO3+...
     &    +  0.344 * RXRAT( 260 )      ! RNO3=0.344*HO2+0.554*xHO2+...
     &    +  2.000 * RXRAT( 261 )      ! HOCCHO=2*HO2+CO+HCHO
     &    +  0.830 * RXRAT( 264 )      ! ACROLEIN+O3=0.83*HO2+...
     &    +  1.066 * RXRAT( 267 )      ! ACROLEIN=1.066*HO2+...
     &    +          RXRAT( 474 )      ! zRNO3+NO3=HO2+PRD2+NO3
     &    +  0.500 * RXRAT( 475 )      ! zRNO3+MEO2=0.5*HO2+...
     &    +  0.500 * RXRAT( 476 )      ! zRNO3+RO2C=0.5*HO2+...
     &    +  0.500 * RXRAT( 477 )      ! zRNO3+RO2XC=0.5*HO2+...
     &    +          RXRAT( 478 )      ! zRNO3+MECO3=HO2+PRD2+MECO3
     &    +          RXRAT( 479 )      ! zRNO3+RCO3=HO2+PRD2+RCO3
     &    +          RXRAT( 480 )      ! zRNO3+BZCO3=HO2+PRD2+BZCO3
     &    +          RXRAT( 481 )      ! zRNO3+MACO3=HO2+PRD2+MACO3
     &    +  0.160 * RXRAT( 504 )      ! ETHENE+O3=0.16*HO2+0.16*OH+...
     &    +  0.800 * RXRAT( 506 )      ! ETHENE+O3P=0.8*HO2+...
     &    +  0.165 * RXRAT( 508 )      ! PROPENE+O3=0.165*HO2+...
     &    +  0.080 * RXRAT( 512 )      ! BUTADIENE13+O3=0.08*HO2+...
     &    +  0.250 * RXRAT( 514 )      ! BUTADIENE13+O3P=0.25*HO2+...
     &    +  0.066 * RXRAT( 515 )      ! ISOPRENE+O3=0.066*HO2+...
     &    +  0.009 * RXRAT( 518 )      ! APIN+O3=0.009*HO2+0.728*OH+...
     &    +  1.500 * RXRAT( 522 )      ! ACETYLENE+O3=1.5*HO2+...
     &    +  0.095 * RXRAT( 536 )      ! OLE1+O3=0.095*HO2+0.128*OH+...
     &    +  0.094 * RXRAT( 540 )      ! OLE2+O3=0.094*HO2+0.443*OH+...
     &    +  0.078 * RXRAT( 546 )      ! TERP+O3=0.078*HO2+0.499*OH+...
     &    +  0.078 * RXRAT( 550 )      ! SESQ+O3=0.078*HO2+0.499*OH+...
     &    +          RXRAT( 574 )      ! CL=HO2+HCL
     &    +          RXRAT( 575 )      ! HCHO+CL=HO2+HCL+CO
     &    +          RXRAT( 577 )      ! MEOH+CL=HO2+HCHO+HCL
     &    +  0.055 * RXRAT( 581 )      ! RNO3+CL=0.055*HO2+...
     &    +  0.314 * RXRAT( 582 )      ! PRD2+CL=0.314*HO2+HCL+...
     &    +  0.630 * RXRAT( 583 )      ! GLY+CL=0.63*HO2+HCL+...
     &    +  0.148 * RXRAT( 589 )      ! RAOOH+CL=0.148*HO2+...
     &    +  0.084 * RXRAT( 592 )      ! IPRD+CL=0.084*HO2+...
     &    +          RXRAT( 593 )      ! CLCCHO=HO2+CO+RO2C+xCL+...
     &    +          RXRAT( 633 )      ! ACETYLENE+CL=HO2+CO
     &    +  0.688 * RXRAT( 639 )      ! ETOH+CL=0.688*HO2+HCL+...
     &    +  0.803 * RXRAT( 673 )      ! ISOPO2+NO=0.803*HO2+...
     &    +  0.950 * RXRAT( 675 )      ! ISOPO2+MEO2=0.5*HO2+...
     &    +  0.450 * RXRAT( 676 )      ! ISOPO2+RO2C=0.45*HO2+...
     &    +  0.910 * RXRAT( 677 )      ! ISOPO2+ISOPO2=0.91*HO2+...
     &    +  0.910 * RXRAT( 678 )      ! ISOPO2+MECO3=0.91*HO2+CO2+...
     &    +          RXRAT( 679 )      ! ISOPO2=HO2+HPALD
     &    +          RXRAT( 680 )      ! HPALD=HO2+OH+0.5*HACET+...
     &    +  0.800 * RXRAT( 683 )      ! NISOPO2+NO3=0.8*HO2+...
     &    +  0.800 * RXRAT( 684 )      ! NISOPO2+NO=0.8*HO2+...
     &    +  0.900 * RXRAT( 686 )      ! NISOPO2+MEO2=0.4*HO2+...
     &    +  0.400 * RXRAT( 687 )      ! NISOPO2+RO2C=0.4*HO2+...
     &    +  0.800 * RXRAT( 688 )      ! NISOPO2+NISOPO2=0.8*HO2+...
     &    +  0.800 * RXRAT( 689 )      ! NISOPO2+MECO3=0.8*HO2+CO2+...
     &    +          RXRAT( 691 )      ! HC5OO+NO=HO2+0.234*HOCCHO+...
     &    +          RXRAT( 693 )      ! HC5OO+MEO2=0.5*HO2+...
     &    +  0.500 * RXRAT( 694 )      ! HC5OO+RO2C=0.5*HO2+...
     &    +          RXRAT( 695 )      ! HC5OO+MECO3=HO2+CO2+...
     &    +  0.600 * RXRAT( 696 )      ! HC5+O3=0.6*HO2+0.79*OH+...
     &    +  0.350 * RXRAT( 698 )      ! ISOPNOOD+NO=0.35*HO2+...
     &    +  0.675 * RXRAT( 700 )      ! ISOPNOOD+MEO2=0.175*HO2+...
     &    +  0.175 * RXRAT( 701 )      ! ISOPNOOD+RO2C=0.175*HO2+...
     &    +  0.350 * RXRAT( 702 )      ! ISOPNOOD+MECO3=0.35*HO2+...
     &    +  0.390 * RXRAT( 703 )      ! ISOPND+O3=0.39*HO2+0.73*OH+...
     &    +  0.400 * RXRAT( 705 )      ! ISOPNOOB+NO=0.4*HO2+...
     &    +  0.700 * RXRAT( 707 )      ! ISOPNOOB+MEO2=0.2*HO2+...
     &    +  0.200 * RXRAT( 708 )      ! ISOPNOOB+RO2C=0.2*HO2+...
     &    +  0.400 * RXRAT( 709 )      ! ISOPNOOB+MECO3=0.4*HO2+CO2+...
     &    +  0.080 * RXRAT( 710 )      ! ISOPNB+O3=0.08*HO2+0.34*OH+...
     &    +          RXRAT( 712 )      ! NIT1NO3OOA+NO3=HO2+PROPNNB+...
     &    +          RXRAT( 713 )      ! NIT1NO3OOA+NO=HO2+PROPNNB+...
     &    +          RXRAT( 716 )      ! NIT1NO3OOA+RO2C=HO2+CO+CO2+...
     &    +  2.000 * RXRAT( 717 )      ! NIT1NO3OOA+MEO2=HO2+CO+CO2+...
     &    +          RXRAT( 718 )      ! NIT1NO3OOA+MECO3=HO2+CO2+...
     &    +  0.500 * RXRAT( 723 )      ! NIT1NO3OOB+MEO2=0.5*HO2+...
     &    +  0.450 * RXRAT( 725 )      ! NIT1+O3=0.45*HO2+0.15*OH+...
